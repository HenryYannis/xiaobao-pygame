# 节奏方块_第1节课_舞台与方块降临.py
# 学习目标：搭建游戏舞台，绘制判定基准线，并实现方块从顶部落下的基础运动

import pygame  # 导入库（是什么），2D游戏心脏（为什么），必需（怎么用）

pygame.init()  # 初始化（是什么），准备引擎（为什么），开头调用（怎么用）
chuangkou = pygame.display.set_mode((400, 600))  # 窗口（是什么），400x600竖屏（为什么），元组坐标（怎么用）
pygame.display.set_caption("Rhythm Block - Lesson 1")  # 标题（是什么），项目名称（为什么），传入文本（怎么用）
shizhong = pygame.time.Clock()  # 时钟（是什么），控制频率（为什么），锁定60FPS（怎么用）

# 基础参数：判定线高度、方块坐标
xian_y = 500  # 判定线位置（是什么），玩家按键的标准线（为什么），固定在屏幕下方（怎么用）
fang_x, fang_y = 175, -50  # 方块位置（是什么），运动主角（为什么），初始在屏幕上方外面（怎么用）

yunxing = True  # 开关（是什么），循环条件（为什么），逻辑判断（怎么用）
while yunxing:  # 主循环（是什么），逻辑中枢（为什么），不断重复执行（怎么用）
    for ev in pygame.event.get():  # 事件监听（是什么），捕捉动作（为什么），轮询读取（怎么用）
        if ev.type == pygame.QUIT: yunxing = False  # 退出判定（是什么），关窗口（为什么），改状态位（怎么用）

    # 逻辑更新：方块向下匀速移动
    fang_y += 5  # 方块位移（是什么），模拟下落（为什么），y坐标累加（怎么用）
    if fang_y > 600: fang_y = -50  # 循环下落（是什么），保持画面有物体（为什么），出界即重置（怎么用）

    chuangkou.fill((30, 30, 30))  # 刷黑背景（是什么），清屏（为什么），填充（怎么用）
    
    # 绘制判定线：一条灰色的水平线
    pygame.draw.line(chuangkou, (100, 100, 100), (0, xian_y), (400, xian_y), 5)  # 画线（是什么），基准参考（为什么），(窗口,颜色,起点,终点,粗细)（怎么用）
    
    # 绘制下落方块：青色正方形
    pygame.draw.rect(chuangkou, (0, 255, 255), (fang_x, fang_y, 50, 50))  # 画方块（是什么），视觉目标（为什么），传入位置宽和高（怎么用）

    pygame.display.flip(); shizhong.tick(60)  # 刷新与定帧（是什么），完成这一帧（为什么），同步频率（怎么用）

pygame.quit()  # 退出（是什么），清理资源（为什么），程序末尾（怎么用）
