# 打砖块_第1节课_小球反弹.py
# 学习目标：掌握pygame基础环境、窗口创建、坐标系概念、小球运动与边缘碰撞反弹

import pygame  # 导入pygame库（是什么），它是Python开发2D游戏的核心工具（为什么），直接import即可使用其功能（怎么用）
import random  # 导入随机数库（是什么），用于生成不确定的数值增加游戏趣味性（为什么），使用random.choice进行随机选择（怎么用）

pygame.init()  # 初始化pygame模块（是什么），准备好声音、显示等所有内部组件（为什么），必须在调用其他功能前第一个运行（怎么用）

chuangkou = pygame.display.set_mode((400, 400))  # 创建游戏显示窗口（是什么），定义游戏运行的可视范围（为什么），传入(宽, 高)像素元组（怎么用）
pygame.display.set_caption("Breakout - Lesson 1")  # 设置窗口顶部的标题（是什么），标识游戏名称和当前课程（为什么），传入字符串即可（怎么用）

shizhong = pygame.time.Clock()  # 创建时钟对象（是什么），用于统一不同电脑上的运行速度（为什么），通过tick方法控制每秒刷新次数（怎么用）

qiu_weizhi = [200, 200]  # 定义小球坐标列表（是什么），存储小球在屏幕上的x和y位置（为什么），[x, y]中x向右变大，y向下变大（怎么用）
qiu_sudu = [random.choice([-3, 3]), random.choice([-3, 3])]  # 定义小球速度列表（是什么），控制小球每帧移动的像素距离（为什么），正负号代表方向（怎么用）

yunxing = True  # 定义程序运行状态（是什么），作为主循环的开关（为什么），设为True保持运行，False则退出（怎么用）
while yunxing:  # 开启游戏主循环（是什么），让游戏画面不断更新（为什么），只要条件为True就会一直重复执行缩进内容（怎么用）
    for shijian in pygame.event.get():  # 获取所有发生的事件（是什么），监听玩家的操作（为什么），通过for循环逐个检查队列里的事件（怎么用）
        if shijian.type == pygame.QUIT:  # 检查是否点击了关闭按钮（是什么），处理用户的退出意图（为什么），判断事件类型是否等于QUIT常量（怎么用）
            yunxing = False  # 修改运行状态为False（是什么），准备结束程序循环（为什么），赋值后while条件将不再成立（怎么用）

    qiu_weizhi[0] += qiu_sudu[0]  # 更新小球的横坐标（是什么），实现小球在水平方向的位移（为什么），将位置加上速度值（怎么用）
    qiu_weizhi[1] += qiu_sudu[1]  # 更新小球的纵坐标（是什么），实现小球在垂直方向的位移（为什么），将位置加上速度值（怎么用）

    if qiu_weizhi[0] <= 10 or qiu_weizhi[0] >= 390:  # 检查左右边界碰撞（是什么），防止小球飞出窗口（为什么），判断x坐标是否超出0-400范围（怎么用）
        qiu_sudu[0] *= -1  # 反转水平速度方向（是什么），实现反弹效果（为什么），正数变负数或负数变正数即改变移动方向（怎么用）

    if qiu_weizhi[1] <= 10 or qiu_weizhi[1] >= 390:  # 检查上下边界碰撞（是什么），实现小球在顶底部的反弹（为什么），判断y坐标是否超出0-400范围（怎么用）
        qiu_sudu[1] *= -1  # 反转垂直速度方向（是什么），让向上变向下或向下变向上（为什么），乘以-1即可取反（怎么用）

    chuangkou.fill((0, 0, 0))  # 用黑色填充整个窗口（是什么），擦除上一帧的旧图像防止重影（为什么），传入(0,0,0)代表黑色RGB值（怎么用）
    
    pygame.draw.circle(chuangkou, (255, 255, 255), qiu_weizhi, 10)  # 在窗口画一个圆（是什么），代表游戏里的球（为什么），参数包含(窗口, 颜色, 坐标, 半径)（怎么用）

    pygame.display.flip()  # 刷新屏幕显示（是什么），将刚才在内存里画好的内容一次性显示出来（为什么），每帧结束前必须调用（怎么用）
    shizhong.tick(60)  # 锁定游戏运行速度（是什么），确保每秒固定运行60帧（为什么），传入60表示60FPS，让动作更流畅（怎么用）

pygame.quit()  # 退出pygame环境（是什么），关闭窗口并释放电脑资源（为什么），循环结束后必须调用以彻底关闭程序（怎么用）
